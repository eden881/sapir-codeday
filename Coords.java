/**
 * Coords
 */
public class Coords {

    private int x;
    private int y;
    private boolean status;

    public Coords(int x, int y, boolean status) {

        this.x = x;
        this.y = y;
        this.status = status;
    }

    public int getX() {

        return x;
    }

    public int getY() {

        return y;
    }

    public boolean getStatus() {

        return status;
    }
}
